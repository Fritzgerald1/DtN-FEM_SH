function N = shape(s,t)
% 形函数
%{
节点编号逆时针：
				7
4	┌───────┐ 3
	│                    │	
8	│                    │6
	│                    │
1	└───────┘2
				5
%}
N1=-0.25*(1-s)*(1-t)*(1+s+t);
N2=0.25*(1+s)*(1-t)*(s-t-1);
N3=0.25*(1+s)*(1+t)*(s+t-1);
N4=0.25*(1-s)*(1+t)*(t-s-1);
N5=0.5*(1-s^2)*(1-t);
N6=0.5*(1+s)*(1-t^2);
N7=0.5*(1-s^2)*(1+t);
N8=0.5*(1-s)*(1-t^2);
N=[N1 N2 N3 N4 N5 N6 N7 N8]';
end